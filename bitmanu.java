public class bitmanu {
    public static void main(String[] args) {
        int a=3;
        int b=5;
        System.out.println("3 and 5 "+(a & b));
        System.out.println("3 or 5 "+(a | b));
        System.out.println("3 xor 5 "+(a ^ b));
        System.out.println("3 not  "+(~a));
    }
    
}
